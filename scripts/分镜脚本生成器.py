# 分镜脚本生成器 - 健康早报版
# 基于李一帆的9镜头分镜框架，针对健康/医学内容优化

def generate_storyboard(news_items, title, subtitle=""):
    """
    生成早报视频分镜脚本
    
    Args:
        news_items: 新闻内容列表，每个元素是(title, body, category)
        title: 早报标题
        subtitle: 副标题
    
    Returns:
        分镜脚本文本
    """
    
    total_items = len(news_items)
    total_shots = 5 + total_items * 2  # 封面5 + 每条新闻2个镜头
    
    shots = []
    
    # ===== 封面（5个镜头）=====
    shots.append({
        "序号": 1,
        "类型": "封面",
        "景别": "远景",
        "镜头": "固定镜头",
        "光影": "逆光",
        "画面": f"乐城天际线，黄昏时分，逆光勾勒建筑轮廓，天空呈蓝紫渐变，营造专业沉稳氛围",
        "台词": f"「{title}」",
        "备注": "交代整体氛围，开场定调"
    })
    
    shots.append({
        "序号": 2,
        "类型": "封面",
        "景别": "中景",
        "镜头": "缓慢推进",
        "光影": "自然顶光",
        "画面": "精算师在办公桌前，翻阅医疗报告，数据图表在屏幕上映出，专业感十足",
        "台词": subtitle or "用精算师的视角，看懂健康行业",
        "备注": "建立人物IP，强化专业人设"
    })
    
    shots.append({
        "序号": 3,
        "类型": "封面",
        "景别": "特写",
        "镜头": "微拉",
        "光影": "侧光",
        "画面": "手握钢笔，在文件上标注重点，镜头从文件拉开到人物侧脸",
        "台词": "今天的重要内容",
        "备注": "特写细节，增加真实感"
    })
    
    shots.append({
        "序号": 4,
        "类型": "封面",
        "景别": "中景",
        "镜头": "横移",
        "光影": "冷色环境光",
        "画面": "书架/医疗设备/乐城元素依次入画，展示专业背景",
        "台词": "今日摘要",
        "备注": "转场过渡，引入正题"
    })
    
    shots.append({
        "序号": 5,
        "类型": "封面",
        "景别": "特写",
        "镜头": "推镜头",
        "光影": "聚焦光",
        "画面": "数据图表特写，数字跳动或增长箭头，暗示今日内容分量",
        "台词": f"共{total_items}条重要资讯",
        "备注": "信息量预告，制造期待"
    })
    
    # ===== 新闻内容（每条2个镜头）=====
    for i, (news_title, news_body, category) in enumerate(news_items):
        base_num = 5 + i * 2
        
        # 新闻镜头A：标题引入
        category_desc = {
            "新药": "新药审批/研发突破",
            "技术": "治疗技术进展", 
            "政策": "政策利好",
            "数据": "行业数据",
            "事件": "重要事件"
        }.get(category, "行业资讯")
        
        shots.append({
            "序号": base_num,
            "类型": "新闻",
            "景别": "中景",
            "镜头": "推向画面",
            "光影": "冷色顶光",
            "画面": f"{category_desc}相关画面，数据图表或医疗场景",
            "台词": news_title,
            "备注": f"【{category}】强化分类感知"
        })
        
        # 新闻镜头B：内容展开
        shots.append({
            "序号": base_num + 1,
            "类型": "新闻",
            "景别": "特写",
            "镜头": "缓慢拉远",
            "光影": "丁达尔光/逆光",
            "画面": f"核心数据或关键信息特写，配合图表或医疗元素",
            "台词": news_body[:80] + "..." if len(news_body) > 80 else news_body,
            "备注": "一句话概括核心价值"
        })
    
    # ===== 结尾（2个镜头）=====
    last_num = 5 + total_items * 2
    
    shots.append({
        "序号": last_num,
        "类型": "结尾",
        "景别": "中景",
        "镜头": "固定",
        "光影": "温暖侧光",
        "画面": "精算师起身，走向窗边，望向远方，留下思考的背影",
        "台词": "关注健康，精算未来",
        "备注": "情绪落点，传递使命感"
    })
    
    shots.append({
        "序号": last_num + 1,
        "类型": "结尾",
        "景别": "远景",
        "镜头": "拉远",
        "光影": "逆光剪影",
        "画面": "乐城天际线全景，镜头缓缓拉远，建筑在夕阳下形成剪影",
        "台词": "刘一｜精算师聊健康",
        "备注": "品牌落位，收尾"
    })
    
    return shots

def print_storyboard(shots):
    """打印分镜脚本"""
    print("=" * 80)
    print("【早报视频分镜脚本】")
    print("=" * 80)
    
    for shot in shots:
        print(f"\n镜头{shot['序号']} | {shot['类型']}")
        print(f"  景别：{shot['景别']}")
        print(f"  镜头：{shot['镜头']}")
        print(f"  光影：{shot['光影']}")
        print(f"  画面：{shot['画面']}")
        print(f"  台词：{shot['台词']}")
        print(f"  备注：{shot['备注']}")

def generate_prompt(shot):
    """生成AI绘图提示词"""
    prompt = f"{shot['景别']}，{shot['画面']}，{shot['光影']}，{shot['镜头']}，电影感，8K，高清"
    return prompt

